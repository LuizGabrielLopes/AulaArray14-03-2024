//Exemplo 1 Array.pop

let cidades = ['Valinhos', 'Campinas', 'Belo Horizonte']; //Crio o array com três cidades

console.log(cidades); // Exibo as cidades do array

let cidadesPop = cidades.pop(); // Utilizo o pop para remover a última cidade do array

console.log(cidades); // Exibo as cidades após utilizar o pop, excluindo assim a última cidade

console.log(cidadesPop); // Exibo qual cidade foi removida pelo pop

