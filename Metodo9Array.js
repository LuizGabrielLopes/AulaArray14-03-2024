//Exemplo 1 .unshift
let carros = ['Mustang', 'Dodge Charger', 'Camaro']; // Criei o array com três carros

console.log(carros); // Exibi o array com os três primeiros carros sendo Mustang, Dodge Charger, e Camaro

carros.unshift('Lancer', 'Pagani', 'McLaren'); // Usei o carros .unshift para adicionar mais três elementos no ínicio do Array

console.log(carros); // Exibi o novo array com os três primeiros carros sendo Lancer, Pagani, e McLaren

