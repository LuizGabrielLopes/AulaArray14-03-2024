//Exemplo 1 .shift
let plantas = ['Magnolias', 'Hortênsia', 'Ipê']; //Criei o array com o nome de plantas

console.log(plantas); // Exibi o array com todas as plantas

let plantas2 = plantas.shift(); // Criei uma variavel plantas2 e igualei ela ao array das plantas com o .shift para remover o primeiro elemento do array

console.log(plantas2); // Exibi o elemento removido do array das plantas

